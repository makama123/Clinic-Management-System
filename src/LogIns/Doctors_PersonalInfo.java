            PreparedStatement stmt=con.prepareStatement("INSERT INTO `doctor table`(`DoctorID`, `First Name`, `Last Name`, `Age`, `Gender`, `Phone Number`, `Date of Joining`, `Marital Status`, `Address`) VALUES (?,?,?,?,?,?,?,?,?)");
