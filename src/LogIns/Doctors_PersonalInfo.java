            stmt.setString(2,Fname);
            stmt.setString(3,Lname);
            stmt.setString(4,Age);
            stmt.setString(5,Gender);
            stmt.setString(6,PhoneNumber);
            stmt.setString(7,Date);
            stmt.setString(8,Marital);
            stmt.setString(9,Address);


